class Solution {
	public:
		int NthTerm(int n){
		    long long a = 1, mod = 1e9+7;
		    for(int i=1; i<=n; i++)
		      a = ((a * i) + 1) % mod;
		    return (int) a % mod;
		}

};

//{ Driver Code Starts.
int main(){
	int tc;
	cin >> tc;
	while(tc--){
		int n;
		cin >> n;
		Solution obj;
		int ans = obj.NthTerm(n);
		cout << ans << "\n";
	}
	return 0;
}
