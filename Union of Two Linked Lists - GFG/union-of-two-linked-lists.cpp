class Solution
{
    public:
    struct Node* makeUnion(struct Node* head1, struct Node* head2)
    {
        set<int> st;
        Node* h1 = head1, *h2 = head2;
        while(h1 or h2){
            if(h1) st.insert(h1->data), h1 = h1->next;
            if(h2) st.insert(h2->data), h2 = h2->next;
        }
        Node* dummy = new Node(-1);
        Node* temp = dummy;
        
        for(auto &it: st){
            temp->next = new Node(it);
            temp = temp->next;
        }
        return dummy->next;
    }
};


