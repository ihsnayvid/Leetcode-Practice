class Solution {
  public:
    long long int minIncrements(vector<int> arr, int n) {
        sort(arr.begin(), arr.end());
        long long inc = 0, mx = INT_MIN;
        for(auto &i: arr){
            if(mx < i) mx = i;
            else{
                mx ++;
                inc += (mx - i);
            }
        }
        return inc;
    }
};

